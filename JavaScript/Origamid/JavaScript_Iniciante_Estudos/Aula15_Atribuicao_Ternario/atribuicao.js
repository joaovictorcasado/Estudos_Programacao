// Operadores de Atribuição:

var x = 5;
var y = 10;


// Servem para simplificar operações 
// Ao utilizar um operador aritmetico anterior a um sinal de igual é como 
// Se estivessemos somando um valor a outro valor e ja colocando o resultado na mesma linha..

// Exemplos dos Operadores de Atribuição:

x+= y;    // 15
x-= y;   // -5
x *= y   //50
x /= y   //2
x %= y  //0
x **= y   //9765625


